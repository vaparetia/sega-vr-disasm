; ============================================================================
; Set Game State $34
; ROM Range: $0033E4-$0033EA (8 bytes)
; ============================================================================
; Sets game state byte at $C305 to $34.
;
; Entry: none
; Uses: none
; ============================================================================

set_state_0x34:
        dc.w    $11FC,$0034,$C305     ; MOVE.B #$34,($C305).W
        rts
