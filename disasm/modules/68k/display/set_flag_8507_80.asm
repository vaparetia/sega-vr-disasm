; ============================================================================
; Set Display Flag $80
; ROM Range: $002474-$00247A (8 bytes)
; ============================================================================
; Sets display mode flag at $8507 to $80.
;
; Entry: none
; Uses: none
; ============================================================================

set_flag_8507_80:
        dc.w    $11FC,$0080,$8507     ; MOVE.B #$80,($8507).W
        rts
